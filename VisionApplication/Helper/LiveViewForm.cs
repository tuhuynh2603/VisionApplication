    public bool BeginReceive()
    {
        lock (_lockEvent)
        {
            if (_disposed)
            {
                return false;
            }

            if (initializeTargetReader())
            {
                _vncClient.BinningType = (VncImageBinningType)BinningType;
                _vncClient.ImageFormat = (VncImageFormat)ImageFormat;
                _vncClient.ImageQuality = ImageQuality;
                _vncClient.Timeout = TimeoutMs;
                _vncClient.MaxImageAreaSize = new Size(2048, 1536);
