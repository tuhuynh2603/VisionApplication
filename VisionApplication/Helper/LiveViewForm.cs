    public bool DownloadRecentImage(string dstFile)
    {
        lock (_lockEvent)
        {
            if (_disposed)
            {
                return false;
            }

            try
            {
                _vncClient.IsEnabledGetAlternateImage = true;
                using (AlternateImage alternateImage = _vncClient.GetAlternateImage())
                {
                    using FileStream fileStream = new FileStream(dstFile, FileMode.Create, FileAccess.Write);
                    byte[] buffer = new byte[alternateImage.ImageStream.Length];
                    alternateImage.ImageStream.Read(buffer, 0, (int)alternateImage.ImageStream.Length);
                    fileStream.Write(buffer, 0, (int)alternateImage.ImageStream.Length);
                }

                _vncClient.IsEnabledGetAlternateImage = false;
            }
            catch (Exception)
            {
                return false;
            }

            return true;
        }
    }

    private bool initializeTargetReader()
    {
        System.Net.IPAddress address = System.Net.IPAddress.None;
        int result = -1;
        if (IpAddress == null)
        {
            return false;
        }

        if (System.Net.IPAddress.TryParse(IpAddress, out address))
        {
            _vncClient.IPAddress = address;
            _vncClient.InitializeProtocol(VncProtocolType.Ether);
            _vncClient.ImageRectangle = new Rectangle(0, 0, 800, 600);
            return true;
        }

        if (Regex.IsMatch(IpAddress, "USBPID:(\\d)") && int.TryParse(Regex.Replace(IpAddress, "USBPID:(\\d)", "$1"), out result))
        {
            _vncClient.UsbPid = result;
            _vncClient.InitializeProtocol(VncProtocolType.USB);
            _vncClient.ImageRectangle = new Rectangle(0, 0, 800, 600);
            return true;
        }

        return false;
    }

